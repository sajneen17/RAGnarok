def visualize_query_results(query, top_k=10):
    results = retrieve(query, top_k=top_k)

    query_embedding = embedding_model.encode([query], convert_to_numpy=True)
    retrieved_embeddings = np.array([chunk_embeddings[res['index']] for res in results])

    all_embeddings = np.vstack([query_embedding, retrieved_embeddings])

    pca = PCA(n_components=2)
    embeddings_2d = pca.fit_transform(all_embeddings)

    plt.figure(figsize=(10, 6))

    plt.scatter(embeddings_2d[1:, 0], embeddings_2d[1:, 1],
                c='steelblue', s=100, alpha=0.6, label='Retrieved Chunks', edgecolors='black')

    plt.scatter(embeddings_2d[0, 0], embeddings_2d[0, 1],
                c='red', s=200, marker='*', label='Query', edgecolors='black', linewidths=2)

    for i, (x, y) in enumerate(embeddings_2d[1:]):
        plt.annotate(f'#{i+1}', (x, y), fontsize=8, ha='center', va='bottom')

    plt.title(f'Query Embedding vs Retrieved Chunks (PCA)\nQuery: "{query[:80]}..."', fontsize=12)
    plt.xlabel('PCA Component 1')
    plt.ylabel('PCA Component 2')
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.show()

def visualize_distance_scores(query, top_k=10):
    results = retrieve(query, top_k=top_k)

    distances = np.array([res['distance'] for res in results])
    labels = [f"Chunk {i+1}" for i in range(len(results))]

    plt.figure(figsize=(10, 5))

    max_dist = distances.max()
    normalized = distances / max_dist if max_dist > 0 else distances
    colors = plt.cm.RdYlGn_r(normalized)
    bars = plt.bar(labels, distances, color=colors, edgecolor='black')

    for bar, dist in zip(bars, distances):
        plt.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.02,
                f'{dist:.3f}', ha='center', va='bottom', fontsize=9)

    plt.title(f'Similarity Distance Scores (Lower = More Similar)\nQuery: "{query[:80]}..."', fontsize=12)
    plt.xlabel('Retrieved Chunks')
    plt.ylabel('L2 Distance')
    plt.xticks(rotation=45)
    plt.grid(True, alpha=0.3, axis='y')
    plt.tight_layout()
    plt.show()

def visualize_tsne_clusters(queries_list, top_k=10):
    all_embeddings = []
    all_labels = []

    for i, q in enumerate(queries_list):
        q_emb = embedding_model.encode([q], convert_to_numpy=True)
        results = retrieve(q, top_k=top_k)
        r_embs = np.array([chunk_embeddings[res['index']] for res in results])

        all_embeddings.append(q_emb[0])
        all_labels.append(f'Q{i+1}: {q[:30]}')

        all_embeddings.extend(r_embs)
        all_labels.extend([f'Q{i+1} Results'] * len(r_embs))

    all_embeddings = np.array(all_embeddings)

    tsne = TSNE(n_components=2, random_state=42, perplexity=min(30, len(all_embeddings)-1))
    embeddings_tsne = tsne.fit_transform(all_embeddings)

    plt.figure(figsize=(14, 8))

    unique_labels = list(dict.fromkeys(all_labels))
    colors_list = ['red', 'blue', 'green', 'orange', 'purple', 'brown']

    for label in unique_labels:
        indices = [j for j, l in enumerate(all_labels) if l == label]
        if label.startswith('Q'):
            color_idx = int(label[1]) - 1 if label[1].isdigit() else 0
            plt.scatter(embeddings_tsne[indices, 0], embeddings_tsne[indices, 1],
                       s=200, marker='*', label=label, c=colors_list[color_idx % len(colors_list)],
                       edgecolors='black', linewidths=2)
        else:
            color_idx = int(label[1]) - 1 if label[1].isdigit() else 0
            plt.scatter(embeddings_tsne[indices, 0], embeddings_tsne[indices, 1],
                       s=50, alpha=0.4, label=label, c=colors_list[color_idx % len(colors_list)])

    plt.title('t-SNE Visualization of Query and Retrieved Embeddings', fontsize=14)
    plt.xlabel('t-SNE Component 1')
    plt.ylabel('t-SNE Component 2')
    plt.legend(bbox_to_anchor=(1.05, 1), loc='upper left')
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.show()

def visualize_similarity_heatmap(query, top_k=10):
    results = retrieve(query, top_k=top_k)

    retrieved_embeddings = np.array([chunk_embeddings[res['index']] for res in results])
    query_embedding = embedding_model.encode([query], convert_to_numpy=True)
    all_embeddings = np.vstack([query_embedding, retrieved_embeddings])

    similarity_matrix = np.zeros((top_k + 1, top_k + 1))

    for i in range(len(all_embeddings)):
        for j in range(len(all_embeddings)):
            similarity = np.dot(all_embeddings[i], all_embeddings[j]) / (
                np.linalg.norm(all_embeddings[i]) * np.linalg.norm(all_embeddings[j])
            )
            similarity_matrix[i, j] = similarity

    labels = ['Query'] + [f'Chunk {i+1}' for i in range(top_k)]

    plt.figure(figsize=(10, 8))
    sns.heatmap(similarity_matrix, annot=True, fmt='.2f', cmap='coolwarm',
                xticklabels=labels, yticklabels=labels, vmin=-1, vmax=1,
                square=True, cbar_kws={'label': 'Cosine Similarity'})
    plt.title(f'Similarity Heatmap: Query vs Retrieved Chunks\nQuery: "{query[:60]}..."', fontsize=12)
    plt.tight_layout()
    plt.show()

