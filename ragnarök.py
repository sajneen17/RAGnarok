"""# **RAG Generation Function**"""

def rag_generate(query, top_k=5):
    retrieved_docs = retrieve(query, top_k)

    context = "\n".join([f"Passage {i+1}: {doc['chunk']}"
                         for i, doc in enumerate(retrieved_docs)])

    prompt = f"""Based on the following context, answer the question accurately.
If the answer is not in the context, say "I don't have enough information."

Context:
{context}

Question: {query}

Answer:"""

    response = generator(
        prompt,
        max_new_tokens=100,
        do_sample=False,
        temperature=0.1
    )[0]['generated_text']

    answer = response.split("Answer:")[-1].strip()

    return {
        "question": query,
        "answer": answer,
        "retrieved_sources": retrieved_docs
    }

