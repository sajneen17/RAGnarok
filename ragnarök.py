def evaluate_retrieval(query, relevant_keywords):
    results = retrieve(query, top_k=10)
    hits = 0
    for res in results:
        for keyword in relevant_keywords:
            if keyword.lower() in res['chunk'].lower():
                hits += 1
                break
    recall = hits / len(relevant_keywords) if relevant_keywords else 0
    return {"query": query, "recall@10": min(recall, 1.0), "hits": hits}

